// ---Conditional operators---

// IF:
/*
    if (a) {
        <...>
    } else if (b) {
        <...>
    } else {
        <...>
    }
*/

// Switch:
/*
    switch (a) {
        case a1:
            <...>
            break;
        case a2:
            <...>
            break;
        default: 
            <...>
            break;
    }
*/

// ---Loop operators---

// FOR:
/*
    for (int/long int/size_t/<...> a = 0; a < / > / <= / >= / == / <...> b; a++/--/+=/-=/*=/ /= / %=) {
        <...>
    }
*/

// WHILE:
/* while (statement) {
    <...>
}
*/

// DO WHILE:
/*
    do {<...>} while (statement)
*/

// The difference between WHILE and DO WHILE is that in WHILE, the iteration is compared with the statement before code execution,
// and in DO WHILE, the iteration is compared after the code execution
