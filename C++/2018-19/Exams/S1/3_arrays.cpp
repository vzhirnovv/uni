// Arrays, their declaration and initialization

// type array[elements];
// int arr[5]; spawns an array of 5 ints
// arr = {1, 2, 3, 4, 5} lets us fill an array
// int arr[5] = {} spawns an array of 5 zeroes
// arr[i] allows to get array element by index
// arr[a][b] spawn a two-dimensional array with a rows and b columns
// func(arr[]) allows to pass array as parameter of a function